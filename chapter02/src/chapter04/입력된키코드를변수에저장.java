package chapter04;

public class 입력된키코드를변수에저장 {

	public static void main(String[] args) throws Exception{
		int kC;
		
		kC = System.in.read();
		System.out.println("keyCode : " + kC);

		kC = System.in.read();
		System.out.println("keyCode : " + kC);

		kC = System.in.read();
		System.out.println("keyCode : " + kC);
				
	}
}
