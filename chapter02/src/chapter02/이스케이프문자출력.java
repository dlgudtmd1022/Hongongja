package chapter02;

public class 이스케이프문자출력 {

	public static void main(String[] args) {
		// \t 탭만큼 띄움
		System.out.println("번호\t이름\t직업");
		// \n 줄 바꿈
		System.out.print("형 단위 출력\n");
		System.out.print("형 단위 출력\n");		
		// \" "출력
		System.out.println("우리는\"개발자\"입니다.");
		// \\ \출력
		System.out.print("봄\\여름\\가을\\겨울");
	}
}
