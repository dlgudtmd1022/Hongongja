package chapter02;

public class 정수리터럴 {

	public static void main(String[] args) {
		// 다양한 정수 리터럴을 int타입에 저장
		int v1 = 0b1011; //2 
		int v2 = 0206;  //8
		int v3 = 365; //10
		int v4 = 0xB3; //16
				
		//10진수에 출력
		System.out.println("v1:" + v1);
		System.out.println("v2:" + v1);
		System.out.println("v3:" + v1);
		System.out.println("v4:" + v1);
	}
}