package chapter02;

public class 대입연산자 {

	public static void main(String[] args) {
		
		int r = 0;
		r += 10;
		System.out.println("result = " + r);
		r -= 5;
		System.out.println("result = " + r);
		r *= 3;
		System.out.println("result = " + r);
		r /= 5;
		System.out.println("result = " + r);
		r %= 3;
		System.out.println("result = " + r);
	}
}
